// 1. Add Two Numbers
const addNumbers = (a, b) => a + b;
console.log(addNumbers(5, 10)); 

// 2. Check If a Number is Positive
const isPositive = num => num > 0;
console.log(isPositive(5)); 
console.log(isPositive(-10)); 

// 3. Get First Character
const getFirstCharacter = str => str[0];
console.log(getFirstCharacter("Hello World!")); 

// 4. Double a Number
const doubleNumber = num => num * 2;
console.log(doubleNumber(5)); 

// 5. Create a Greeting Message
const createGreetingMessage = name => `Hello ${name}!`;
console.log(createGreetingMessage("Nodiko Bejanidze"));
